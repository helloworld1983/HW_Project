	ldr sp, TEMP			; 스택의 주소를 TEMP로 함
	
	mov r4, r0  				; r4에 Random_data의 주소를 넘김
	sub r4, r4, #4 			; 시작주소를 4byte 내림 
	

	mov r5, #250
	mov r5, r5, lsl #4
	add r5, r5, r4			 ; r5 = 끝 주소 
	
	b oper ; jump oper
	
	
pop_p

	ldr r4, [sp]			;;; 새 블럭의 시작 주소를 stack에서 받아옴
	sub sp, sp, #4
	cmp sp, #0x10000000  ; SP의 위치가 초기 stack pointer의 위치보다 낮아지면 SAVE_DATA로 이동함.
	blt	SAVE_DATA 
	ldr r5, [sp] 			;;; 새 블럭의 끝 주소를 stack에서 받아옴
	sub sp, sp, #4
	
oper
	mov r10, r4  ; r10 = r4의 포인터 역할
	mov r11, r5  ; r11 = r5의 포인터 역할 
	
	;;;;;;;;;;;;;; R7 = I의 주소 , R8 = J의 주소, R9 = Pivot의 주소로 씀;;;;;;;;;;;;;;;;;
	
	ldr r9, [r11] ; Pivot
	
	sub r12, r5, r4  ; 현재 블럭의 끝주소와 시작주소의 차이가 4 이하이면 pop할것임.
	cmp r12, #4  
	ble pop_p ; 
	
shift_I 					 ; I가 pivot보다 작은 경우 I pointer를 오른쪽으로 1칸 이동시킴 ( I가 pivot과 같아지면 Shift_J로 넘어감)
	add r4, r4, #4 
	ldr r8, [r4] 
	
	mov r12, r8, lsr #31	; I의 sign bit 추출
	mov r3, r9, lsr #31	; Pivot의 sign bit 추출
	cmp r12, r3				; 두 sign bit 비교
	bgt	shift_I				; I sign bit =1 / pivot sign bit =0	이면 I가 더 작기 떄문에 shift I
	blt	shift_J				; I sign bit =0 / pivot sign bit =1 이면 I가 더 크기 때문에 Shift J

	cmp r12, #1				; 여기 까지 왔다면 두 sign bit이 같다는 것인데, 둘다 1인가?
	beq	one_case_I		; 그러하면 one_case_I로 이동
	
	; 여기까지 왔다면 두 sign bit이 0(양수)

	cmp r8, r9				; I와 Pivot 비교 어차피 둘다 양수이고 exponent가 클수록 값도 더클테니까 바로 비교해도 ㄴ 상관
	blt shift_I					; I가 더작으면 I를 오른쪽으로 이동시킴
	bge	shift_J				;서로 같거나 I가 더 크면 shift_J로 이동
	
one_case_I					;  I와 pivot이 둘다 음수

	cmp r8, r9				; I와 Pivot를 비교함
	bgt shift_I					; I가 더 크면 결국 전체값도 작은거니까 shift_I로 이동하셈
	
	; 드디어 I가 pivot과 같거나 크다는 것을 증명했어요 I는 자유로운 영혼이에요 shift_J로 갈꺼랍니당 ㅇㅅㅇ
	

shift_J ; I가 pivot과 같거나 크지만 J가 pivot보다 작지 않을 때, J포인터를 왼쪽으로 1칸 이동시킴
	sub r5, r5, #4  			; J의 위치는 pivot의 바로 왼쪽
	ldr r7, [r5] 				; J의 실값

 	mov r12, r7, lsr #31	; J의 sign bit 추출
	mov r3, r9, lsr #31	; Pivot의 sign bit 추출
	cmp r12, r3				; 두 sign bit 비교
	blt	shift_J				; J sign bit =0 / pivot sign bit =1	이면 J가 더 크기 떄문에 shift J
	bgt	Swap				; J sign bit =1 / pivot sign bit =0 이면 J가 더 작기 때문에 I_J를 swap하러 갈꺼임
	
	cmp r12, #1				; 여기 까지 왔다면 두 sign bit이 같다는 것인데, 둘다 1인가 ?
	beq	one_case_J		; 그러하면 one_case_J로이동
	
	; 여기부터는 둘 다 양수인 case

	cmp r7, r9				; J와 Pivot 비교함
	bgt	shift_J				; J가 크면 shift_J 로 돌아감
	ble	Swap				; J의 exponent가 작으면 전체 값도 작은거니까 swap하러 감


; 여기부터는 J와 Pivot 둘다 음수임
one_case_J

	cmp r7, r9				; J와 Pivot 비교함
	blt	shift_J				; J가 작으면 전체값이 크다는 얘기니까  shift_J 로 돌아감

	; 여기 까지 오셨다면  J가 pivot과 같거나 크다는 것이므로 J. pivot swap

	
Swap ; I와 J를 swap
	cmp r4, r5  
	bge change_end  ; r4가 r5보다 뒤쪽에 존재하거나 같은 위치에 있다면 끝 주소를 바꾸기 위해 change_end로 이동
	
	; r4가 r5보다 작으면 서로 swap
	str r8, [r5]
	str r7, [r4]

	b shift_I ; shift_I로 이동
	
change_end 

	str r8, [r11]				; 현재 I와 pivot을 swap하고 
	str r9, [r4] 
	
	stmfa sp!, {r11}	 ;; 오른쪽 블럭의 끝 주소를 stack에 넣음
	stmfa sp!, {r4}		 ;; 오른쪽 블럭의 시작 주소-4를 stack에 넣음(shift_I에서 시작할떄 I를 시작주소 +4로 잡으므로)

	sub r5, r4, #4 		 ;; 왼쪽 블럭의 끝주소 셋팅
	mov r4, r10			 ;; 왼쪽 블럭의 시작주소 리셋
	
	b oper 					 ;; oper로 넘어감


SAVE_DATA ;; save는 10개씩 10번에 걸쳐서 저장함 
	mov r2, #0 ;; 갯수 카운터
loop
	cmp r2, #100		;; counter가 the_end로 탈출함.
	beq THE_END
	ldr r3, [r0], #4
	str r3, [r1], #4
	ldr r3, [r0], #4
	str r3, [r1], #4
	ldr r3, [r0], #4
	str r3, [r1], #4
	ldr r3, [r0], #4
	str r3, [r1], #4
	ldr r3, [r0], #4
	str r3, [r1], #4
	ldr r3, [r0], #4
	str r3, [r1], #4
	ldr r3, [r0], #4
	str r3, [r1], #4
	ldr r3, [r0], #4
	str r3, [r1], #4
	ldr r3, [r0], #4
	str r3, [r1], #4
	ldr r3, [r0], #4
	str r3, [r1], #4
	
	add r2, r2, #1		;; counter값 1 증가
	b loop
	
THE_END
	mov pc, #0			; Program end
